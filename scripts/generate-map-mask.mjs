import { Jimp } from 'jimp';
import fs from 'fs';

const IMAGE_PATH = 'public/bg-map_mask.jpg';
const OUTPUT_PATH = 'src/data/MapMaskData.ts';

async function generateMask() {
    console.log("Reading mask image...");
    const image = await Jimp.read(IMAGE_PATH);
    const width = image.bitmap.width;
    const height = image.bitmap.height;
    console.log(`Processing image size: ${width}x${height}`);
    
    const rows = [];
    
    for (let y = 0; y < height; y++) {
        let rowStr = '';
        for (let x = 0; x < width; x++) {
            const idx = (y * width + x) * 4;
            const r = image.bitmap.data[idx + 0];
            const g = image.bitmap.data[idx + 1];
            const b = image.bitmap.data[idx + 2];
            
            const threshold = 128;
            const isR = r > threshold;
            const isG = g > threshold;
            const isB = b > threshold;
            
            let char = '_'; // Default to Sea
            if (isR && isG && isB) char = 'P'; // White -> Plains
            else if (isR && !isG && !isB) char = 'V'; // Red -> Volcano
            else if (!isR && isG && !isB) char = 'F'; // Green -> Forest
            else if (!isR && !isG && isB) char = 'S'; // Blue -> Snow
            else if (isR && isG && !isB) char = 'D'; // Yellow -> Desert
            
            rowStr += char;
        }
        rows.push(`    "${rowStr}"`);
    }

    const tsContent = `// Auto-generated by scripts/generate-map-mask.mjs
import { TerrainType } from '../models/types';

const MASK_ROWS = [
${rows.join(',\n')}
];

const WIDTH = ${width};
const HEIGHT = ${height};

export class MapMaskData {
    /**
     * Get terrain type at percentage coordinates (0-100)
     */
    public static getTerrainAt(pctX: number, pctY: number): TerrainType | null {
        // Clamp percentages
        pctX = Math.max(0, Math.min(100, pctX));
        pctY = Math.max(0, Math.min(100, pctY));
        
        const x = Math.floor((pctX / 100) * (WIDTH - 1));
        const y = Math.floor((pctY / 100) * (HEIGHT - 1));
        
        const row = MASK_ROWS[y];
        if (!row) return null;
        const char = row[x];
        
        switch (char) {
            case 'P': return TerrainType.PLAINS;
            case 'F': return TerrainType.FOREST;
            case 'S': return TerrainType.SNOW_MOUNTAIN;
            case 'V': return TerrainType.VOLCANO;
            case 'D': return TerrainType.DESERT;
            default: return null;
        }
    }
}
`;

    fs.writeFileSync(OUTPUT_PATH, tsContent);
    console.log(`Successfully generated ${OUTPUT_PATH}`);
}

generateMask().catch(console.error);
